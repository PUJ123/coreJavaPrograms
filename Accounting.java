package com.app.model;

public class Accounting {
	private String start;
	private String end;
	public Accounting() {
		super();
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}


}
